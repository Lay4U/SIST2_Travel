package travel.spot;

public class RoomDTO {

    private String roomsSeq;
    private String name;
    private String explain;
    private String address;
    private String type;
    private String rank;
    private String price;
    private String grade;
    private String roomsX;
    private String roomsY;
    private String wish;
    private String siteSeq;

    public String getRoomsSeq() {
        return roomsSeq;
    }

    public void setRoomsSeq(String roomsSeq) {
        this.roomsSeq = roomsSeq;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getExplain() {
        return explain;
    }

    public void setExplain(String explain) {
        this.explain = explain;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getRoomsX() {
        return roomsX;
    }

    public void setRoomsX(String roomsX) {
        this.roomsX = roomsX;
    }

    public String getRoomsY() {
        return roomsY;
    }

    public void setRoomsY(String roomsY) {
        this.roomsY = roomsY;
    }

    public String getWish() {
        return wish;
    }

    public void setWish(String wish) {
        this.wish = wish;
    }

    public String getSiteSeq() {
        return siteSeq;
    }

    public void setSiteSeq(String siteSeq) {
        this.siteSeq = siteSeq;
    }
}